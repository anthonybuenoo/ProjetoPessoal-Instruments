
var sentimentoModel = require("../models/sentimentoModel");


var sessoes = [];


function feliz(req, res) {
    var emocao = req.body.felizServer;
  

        sentimentoModel.feliz(emocao)
            .then(
                function (resultado) {
                    res.json(resultado);
                }
            ).catch(
                function (erro) {
                    console.log(erro);
                    console.log(
                        "\nHouve um erro ao realizar o cadastro! Erro: ",
                        erro.sqlMessage
                    );
                    res.status(500).json(erro.sqlMessage);
                }
            );
    
}

